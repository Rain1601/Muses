const ImagePasteHandler = Extension.create({
  name: 'imagePasteHandler',

  addOptions() {
    return {
      uploadFunction: uploadImageToGitHub,
    };
  },

  addProseMirrorPlugins() {
    const extensionThis = this;
    
    return [
      new Plugin({
        key: new PluginKey('imagePasteHandler'),
        props: {
          handlePaste: (view, event) => {
            const items = Array.from(event.clipboardData?.items || []);
            const imageItems = items.filter(item => item.type.startsWith('image/'));

            if (imageItems.length > 0) {
              event.preventDefault();
              
              imageItems.forEach(async (item) => {
                const file = item.getAsFile();
                if (file) {
                  try {
                    // ÊòæÁ§∫‰∏ä‰º†‰∏≠Áä∂ÊÄÅ
                    const { tr } = view.state;
                    const pos = view.state.selection.from;
                    
                    // ÊèíÂÖ•‰∏¥Êó∂Âç†‰ΩçÁ¨¶
                    tr.insertText('üì∏ ‰∏ä‰º†ÂõæÁâá‰∏≠...', pos);
                    view.dispatch(tr);
                    
                    // ‰∏ä‰º†ÂõæÁâá
                    const imageUrl = await extensionThis.options.uploadFunction(file);
                    
                    // ÊõøÊç¢Âç†‰ΩçÁ¨¶‰∏∫ÂÆûÈôÖÂõæÁâá
                    setTimeout(() => {
                      const { state } = view;
                      const currentPos = state.selection.from;
                      const newTr = state.tr;
                      
                      // Âà†Èô§Âç†‰ΩçÁ¨¶ÊñáÊú¨
                      newTr.delete(currentPos - 11, currentPos);
                      
                      // ÊèíÂÖ•ÂõæÁâáËäÇÁÇπ
                      const imageNode = state.schema.nodes.image.create({
                        src: imageUrl,
                        alt: file.name,
                        title: file.name
                      });
                      
                      newTr.insert(currentPos - 11, imageNode);
                      view.dispatch(newTr);
                    }, 100);
                    
                  } catch (error) {
                    console.error('ÂõæÁâá‰∏ä‰º†Â§±Ë¥•:', error);
                  }
                }
              });
              
              return true;
            }
            
            return false;
          },
          
          handleDrop: (view, event) => {
            const files = Array.from(event.dataTransfer?.files || []);
            const imageFiles = files.filter(file => file.type.startsWith('image/'));

            if (imageFiles.length > 0) {
              event.preventDefault();
              
              const coordinates = view.posAtCoords({
                left: event.clientX,
                top: event.clientY,
              });
              
              if (coordinates) {
                imageFiles.forEach(async (file) => {
                  try {
                    const imageUrl = await extensionThis.options.uploadFunction(file);
                    
                    setTimeout(() => {
                      const { tr } = view.state;
                      const imageNode = view.state.schema.nodes.image.create({
                        src: imageUrl,
                        alt: file.name,
                        title: file.name
                      });
                      
                      tr.insert(coordinates.pos, imageNode);
                      view.dispatch(tr);
                    }, 100);
                    
                  } catch (error) {
                    console.error('ÂõæÁâá‰∏ä‰º†Â§±Ë¥•:', error);
                  }
                });
              }
              
              return true;
            }
            
            return false;
          },
        },
      }),
    ];
  },
});

